package com.conduit.demo.tutorials.javabasics_part1;

public class Ex_05_ControlStatements_WhileLoop {

	public static void main(String[] args) {
	
		// While Loop
		// Simple while loop - if condition returns to be true it executes until condition becomes false.
	    int i=1;  
	    while(i<=10){  
	        System.out.println(i);  
	        i++;
	    } 

	}

}
