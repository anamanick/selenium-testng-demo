package com.conduit.demo.tutorials.javabasics_part1;

public class Ex_02_DataTypes_doubleDataType {

	public static void main(String[] args) {
		
		/*
		 * Java as two main types of data types;
		 *  	i) Primitive data types
		 *  	ii) Non-primitive data types
		 */
		
		
		/*
		 * i) Primitive data types are;
		 * 		1) byte
		 * 		2) short 
		 * 		3) int
		 * 		4) long
		 * 		5) float
		 * 		6) double  - we are here
		 * 		7) char
		 * 		8) boolean	
		 */
		
		/*
		 * ii) Non-Primitive data types are;
		 * 		1) class
		 * 		2) object
		 * 		3) String
		 * 		4) Array
		 * 		5) Interfaces	
		 */
		
		
		//===========================================
		// Example of 1) double is a double precision data type. it's 64 bit.
		//===========================================
		
		
		double a = 100.5d;
		double b = -10000.76d;
		
			
		// Results in action
		System.out.println("My double data type is : "+ a);
		System.out.println("My double data type is : "+ b);
		System.out.println("My double data addition result : "+ (a - b));
		

	}

}
