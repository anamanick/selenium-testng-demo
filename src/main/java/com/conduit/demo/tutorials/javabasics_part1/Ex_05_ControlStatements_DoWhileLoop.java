package com.conduit.demo.tutorials.javabasics_part1;

public class Ex_05_ControlStatements_DoWhileLoop {

	public static void main(String[] args) {
	
		// Do While Loop
		// It executes at least one time before it check the condition
		int i=1;    
	    do{    
	        System.out.println(i);    
	        i++;    
	    }while(i<=10); 

	}

}
